/**
 * Custom data types specific to ashva namespace
*/
namespace ashva
{
    typedef unsigned char u8_t;
    typedef unsigned short u16_t;
    typedef unsigned int u32_t;
    typedef unsigned long long u64_t;

    typedef float f32_t;
    typedef double f64_t;
    typedef long double f128_t;
};

typedef ashva::u8_t au8;
typedef ashva::u16_t au16;
typedef ashva::u32_t au32;
typedef ashva::u64_t au64;
typedef ashva::f32_t af32;
typedef ashva::f64_t af64;
typedef ashva::f128_t af128;
